exports.getOrders = (req, res, next) => {
  //here we need to include in the query the products associated to the user
  //this is eager loading
  req.user
    .getOrders()
    .then((orders) => {
      res.render("shop/orders", {
        path: "/orders",
        pageTitle: "Your Orders",
        orders: orders,
      });
    })
    .catch((err) => console.log(err.red));
};
